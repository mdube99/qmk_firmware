bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (!process_select_word(keycode, record, SELWORD)) { return false; }
  if (!process_caps_word(keycode, record)) { return false; }

  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case CMD_TAB:
      if (record->event.pressed) {
        // when keycode CMD_TAB is pressed
        SEND_STRING(SS_DOWN(X_LGUI)SS_TAP(X_TAB));
      } else {
        // when keycode CMD_TAB is released
      }
      break;
    case TMUX_LEADER:
      if (record->event.pressed) {
        // when keycode TMUX_LEADER is pressed
        // SEND_STRING(SS_TAP(X_LCTL)SS_TAP(X_A));
        SEND_STRING(SS_LCTL("a"));
      } else {
        // when keycode TMUX_LEADER is released
      }
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);

        // when the layer toggler key for LOWER is released,
        // also release the LGUI key
        SEND_STRING(SS_UP(X_LGUI));
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
  }
  return true;
}
